# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None

class Solution:
    def getDecimalValue(self, head: ListNode) -> int:

        s = ''
        h = head
        while h:
            s += str(h.val)
            h = h.next

        return int(s, 2)
